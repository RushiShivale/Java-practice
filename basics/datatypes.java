public class datatypes {
    public static void main(String args[]){
        // Data types in java : 1.primitive & 2.Non-primitive
        // Non-primitive: String,Array,Class,Object,Interface
        // Primitive: byte,short,char,boolean,int,long,float,double

        byte b = 9;
        System.out.println(b);

        char a = 'r';    // only store one value
        System.out.println(a); 

        boolean var = false; // store only true or false
        System.out.println(var);

        int  numb = 34;
        System.out.println(numb); // long is same as int but used for stored longer value

        float price = 56.20f;
        System.out.println(price); // double is same as float used for stored longer point value

    }

}
